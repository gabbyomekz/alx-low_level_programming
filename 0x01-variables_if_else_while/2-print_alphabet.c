#include <stdlib.h>
#include <time.h>
#include <stdio.h>

/**
 * main - Describes the function declaration
 * print the last digit of the number stored in the variable
 * Return: gives zero when successful
*/

int main(void)
{
	int ch;

	for (ch = 97; ch <= 122; ch++)
	{
		putchar(ch);
	}
	putchar(10);

	return (0);
}
